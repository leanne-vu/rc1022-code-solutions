function isVowel(character) {
  if (character !== 'a' && character !== 'A' && character !== 'e' && character !== 'E' && character !== 'i' && character !== 'I' && character !== 'o' && character !== 'O' && character !== 'u' && character !== 'U') { return false; } else return true;
}
